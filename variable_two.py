#List example
person  = ["john", "mombasa", 98.786]

print(person)

#Add value to a list
person.append(30)

#Change value in list
person[2] = 53
print(person)

#Get values in a list
print("Weight is ",person[2])
print("Age is ", person[3])
print("Location is ", person[-3])

#List slicing
print("Name to Weight is ", person[2:-2])

#length of list
print("Length is ",len(person))

